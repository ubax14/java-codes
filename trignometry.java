public class trignometry {
    public static void main(String[] args) {
        //Exponent methods
        System.out.println("These are exponent methods ");
        System.out.println(Math.pow(3,4));
        System.out.println(Math.exp(1));
        System.out.println(Math.log(2.71));
        System.out.println(Math.sqrt(4));
        System.out.println(Math.sqrt(10.5));
        //Rounded methods
        System.out.println("These are rounded methods");
        System.out.println(Math.ceil(3.7));
        System.out.println(Math.round(3.7));
        System.out.println(Math.rint(3.7));
        System.out.println(Math.floor(3.7));
        System.out.println('a'+'1');
        System.out.println('a'+1);
        System.out.println('a'+"ali");
    }
}
